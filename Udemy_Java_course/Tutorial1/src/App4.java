public class App4 {
	public static void main(String[] args) {
		int i = 6;

		while (i < 10) {
			System.out.println("The value is: " + i);
			i++;
		}

		for (i = 0; i < 10; i++) {
			System.out.println("The value is: " + i + " in for loop.");
		}

		if (i == 10) {
			System.out.println("This is true.");
		}
	}
}
